CREATE TABLE MONHOC(
	TENMH NVARCHAR(50) NOT NULL,
	MAMH VARCHAR(10) NOT NULL,
	TINCHI INT NOT NULL,
	MAKHOA VARCHAR(10) NOT NULL
);
