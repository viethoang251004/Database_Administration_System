ALTER TABLE DIEUKIEN ADD CONSTRAINT FK_MAMH1 FOREIGN KEY(MAMH) REFERENCES MONHOC(MAMH);
ALTER TABLE DIEUKIEN ADD CONSTRAINT FK_MAMH2 FOREIGN KEY(MAMH_TRUOC) REFERENCES MONHOC(MAMH);

